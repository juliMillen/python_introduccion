### OPERADORES ###

print(3 + 4)  #suma

print(3 - 4)  #resta

print(3 * 4)  #multiplicacion

print(3 / 4)  #division

print(10 % 2) #modulo

print(10 // 3)

my_float = 2.5 * 2
print("Hola " * int(my_float))


## Operadores Comparativos ##

print( 3 > 4) #mayor que

print( 3 < 4) #menor que

print( 3 >= 4) #mayor o igual que

print( 3 <= 4) #menor o igual que

print( 3 == 4) #igualdad

print( 3 != 4) #distinto de

#Comparativos con Strings
print("Hola" > "Python")

print("Hola" < "Python")

print("Hola" >= "Python")

print("Hola" <= "Python")

print("Hola" == "Python")

print("Hola" != "Python")

print(len("aaa") >= len("abaa"))


### Operadores Logicos ###


print(3 > 4 and "Hola" > "Python")

print(3 > 4 or "Hola" > "Python")

print(not(3 > 4 and "Hola" > "Python"))